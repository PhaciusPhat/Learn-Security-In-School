    public int kytudacbiet(String s) {
        if (s == null || s.trim().isEmpty()) {
            System.out.println("Incorrect format of string");
            return 0;
        }
        Pattern p = Pattern.compile("[^A-Za-z0-9]");
        Matcher m = p.matcher(s);

        boolean b = m.find();
        if (b == true) {
            return 1;
        } else {
            return 0;
        }
    }

