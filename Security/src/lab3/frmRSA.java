    private void btnEncryptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEncryptActionPerformed
        String hoten = txtHoTen.getText();
        String diachi = txtDiaChi.getText();
        String email = txtEmail.getText();
        String sdt = txtSDT.getText();
        String password = txtPassword.getText();
        if(kytudacbiet(password)==0)
        {
            JOptionPane.showMessageDialog(null,"Hãy nhập kí tự đặc biệt");
            return;
        }
        String s = hoten + " " + diachi + " " + email + " " + sdt + " " + password;
        txtPlainText.setText(s);

        RSA rsa = new RSA(8);
        n = rsa.getN();
        d = rsa.getD();
        ciphertext = rsa.encrypt(s);
        StringBuffer bf = new StringBuffer();
        for (int i = 0; i < ciphertext.length; i++) {
            bf.append(ciphertext[i].toString(16).toUpperCase());
        }
        String message = bf.toString();
        txtCipherText.setText(message);
    }//GEN-LAST:event_btnEncryptActionPerformed

